    private static Dictionary<String, String> labelDictionary = new Dictionary<String, String> ( );

    // =====================================================================================
    /// <summary>
    /// This method reads in the label data file.
    /// </summary>
    /// <param name="FilePath">String: file directory path</param>
    /// <param name="LabelFile">String Label file name</param>
    /// <returns>True: file exists and was read</returns>
    // -------------------------------------------------------------------------------------
    public static bool ReadInLabel ( String FilePath, String LabelFile )
    {
      string fileName = LabelFile + ".csv";

      List<String> rowList =  Evado.Model.Clinical.EvcStatics.Files.readFileAsList ( FilePath, fileName );

      foreach ( String row in rowList )
      {
        string firstRow = row.ToLower ( );

        if ( firstRow.Contains ( "\"label\"" ) == true
          || firstRow.Contains ( "\"value\"" ) == true )
        {
          continue;
        }

        string value = row;
        value = value.Replace( "\"", "" );
        value = value.Replace( ",", "^" );

        string [ ] arrRow = value.Split ( '^' );
        if ( arrRow.Length > 1 )
        {

          labelDictionary.Add ( arrRow [ 0 ], arrRow [ 1 ] );
        }

      }//ENd foreach loop


      return true;
    }//END ReadInLabel method.

    //==================================================================================
    /// <summary>
    /// This method returns the selected label.
    /// </summary>
    /// <param name="LabelKey">String the Label Key</param>
    /// <returns>String: label</returns>
    //-----------------------------------------------------------------------------------
    public static String getLabelValue<T> ( T LabelKey )
    {
      String labelKey = LabelKey.ToString ( );

      return labelDictionary [ labelKey ];
    }

